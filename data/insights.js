// Resolve the right insight for a state object. Clamps to [0,5] so out-of-range
// maturityLevel values never throw.
export function selectInsight(state) {
  const lvl = Math.max(0, Math.min(MATURITY_INSIGHTS.length - 1, Number(state && state.maturityLevel) || 0));
  return MATURITY_INSIGHTS[lvl];
}

// Used by the AI prompt: a compact, model-readable block that the LLM must
// align with (not contradict). This is the "hybrid" half: deterministic
// content, machine-formatted for Gemini's prompt.
export function buildInsightContext(state) {
  const insight = selectInsight(state);
  return [
    `Current maturity level: ${insight.level} of 5 (${insight.title}).`,
    `Deterministic headline: ${insight.headline}`,
    `Deterministic first action (BINDING — your firstAction must align with or elaborate on this, not contradict it): ${insight.firstAction}`,
    `What unlocks the next level: ${insight.unlocksMilestone}`,
    `Why this is the right next move: ${insight.rationale}`,
    `Citation labels you may use to back this: ${insight.evidenceCitations.join(" | ")}`
  ].join("\n");
}