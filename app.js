  async deleteHistoryReport(id) {
    if (!confirm("Are you sure you want to delete this historical report snapshot?")) return;

    try {
      const res = await fetch(`/api/reports/${id}`, { method: "DELETE", credentials: "same-origin" });
      if (!res.ok) {
        throw new Error("Failed to delete report.");
      }
      this.showToast("Report snapshot deleted.");
      this.renderHistoryView();
    } catch (e) {
      this.showToast(`Error: ${e.message}`);
    }
  }

  openGlossaryWithTerm(termKey) {
    // Clear search
    document.getElementById("glossary-search").value = "";
    
    // Populate with highlighted term
    this.populateGlossaryList("", termKey);
    
    // Open modal
    const glossaryModal = document.getElementById("dialog-glossary");
    if (glossaryModal) {
      glossaryModal.showModal();
    }
  }
