  // Build the real company record from the assessment (no navigation).
  finalizeCompanyFromAssessment() {
    const a = this.state.assessment;
    if (!a || this.state.company.isInitialized) return;

    const materiality = [...a.activities]
      .filter(k => ACTIVITIES_DB[k] && ACTIVITIES_DB[k].scope !== "avoided")
      .sort((x, y) => ACTIVITIES_DB[y].defaultVal - ACTIVITIES_DB[x].defaultVal);

    this.state.company = {
      name: a.name,
      url: a.url,
      stage: a.stage,
      businessModel: a.businessModel,
      teamSize: a.teamSize,
      activities: a.activities,
      materiality: materiality,
      isInitialized: true,
      scalingReference: `Climate Brick Reference: ${a.stage === "Pre-seed" || a.stage === "Seed" ? "Brick #1 - Seed Scaling" : "Brick #2 - Series A Acceleration"}`
    };

    this.state.metrics = [];
    a.activities.forEach(actKey => {
      const db = ACTIVITIES_DB[actKey];
      if (db && db.scope !== "avoided") {
        const modeled = a.snapshot && Array.isArray(a.snapshot.breakdown)
          ? a.snapshot.breakdown.find(item => item.id === actKey)
          : null;
        this.state.metrics.push({
          id: db.id, name: db.name, scope: db.scope, value: modeled ? modeled.value : this.scaledActivityValue(db, a.teamSize),
          unit: db.unit, cadence: "annual", source_type: db.type,
          measured_at: new Date().toISOString(), uncertainty: db.defaultUnc
        });
      }
    });

    this.state.goals = [];
    const bmLower = String(a.businessModel || a.inferredBusinessModel || "").toLowerCase();
    const industryGoalKeys = [];
    if (bmLower.includes("hardware") || bmLower.includes("device") || bmLower.includes("physical")) {
      industryGoalKeys.push("hardware-lca");
    }
    if (bmLower.includes("food") || bmLower.includes("agri") || bmLower.includes("farm") || bmLower.includes("beverage") || bmLower.includes("restaurant")) {
      industryGoalKeys.push("food");
    }
    if (bmLower.includes("biotech") || bmLower.includes("medical") || bmLower.includes("lab")) {
      industryGoalKeys.push("biotech");
    }

    [...a.activities, ...industryGoalKeys].forEach(actKey => {
      const template = GOAL_TEMPLATES[actKey];
      if (template) {
        if (this.state.goals.some(g => g.id === `goal-${actKey}`)) return;
        this.state.goals.push({
          id: `goal-${actKey}`, title: template.title, owner_id: "rae",
          status: "Not Started",
          deadline: new Date(Date.now() + 90 * 24 * 60 * 60 * 1000).toISOString().split("T")[0],
          level: "company-wide", evidence: []
        });
      }
    });

    this.state.streak.count = 1;
    this.state.streak.lastUpdate = new Date().toISOString();
    this.updateMaturityLevel();
  }

  // Ask Gemini (server-side) for a preview or full briefing; render it on the report.
  async generateAIReport(mode = "full") {
    const isPreview = mode === "preview";
    const card = document.getElementById("fn-ai-briefing");
    const statusEl = document.getElementById("fn-ai-status");
    const bodyEl = document.getElementById("fn-ai-body");
    card.classList.remove("hidden");
    statusEl.innerHTML = `<span class="ai-spinner-inline"></span> ${isPreview ? "Generating preview…" : "Generating full report…"}`;
    bodyEl.innerHTML = "";

    const finishFull = () => {
      this._readyForDashboard = true;
    };

    try {
      const res = await fetch("/api/generate-report", {
        method: "POST",
        credentials: "same-origin",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ assessment: this.state.assessment, mode })
      });
      const data = await res.json();
      if (!res.ok) {
        card.classList.add("hidden");
        if (!isPreview) finishFull();
        return false;
      }

      if (data.snapshot) {
        this.state.assessment.snapshot = data.snapshot;
        if (data.snapshot.isEnterpriseAI) this.state.assessment.isEnterpriseAI = true;
      }

      if (isPreview) {
        this.state.assessment.aiPreview = data.report;
        if (data.report.inferredBusinessModel) {
          this.state.assessment.inferredBusinessModel = data.report.inferredBusinessModel;
          const bm = data.report.inferredBusinessModel;
          const metaEl = document.getElementById("fn-report-meta");
          if (metaEl) {
            const docNote = this.state.assessment.docs.deck || this.state.assessment.docs.accounting ? " · documents noted" : "";
            metaEl.innerText = `${this.state.assessment.stage || "Stage"} · ${bm} · FTE-scaled modeled estimate${docNote}`;
          }
        }
        this.renderAIBriefing(data.report, { preview: true, quota: data.quota });
        statusEl.innerText = data.quota ? `Preview · ${data.quota.remaining}/${data.quota.limit} left today` : "Preview";
        return true;
      }

      this.state.assessment.aiReport = data.report;
      if (data.report.inferredBusinessModel) {
        this.state.assessment.inferredBusinessModel = data.report.inferredBusinessModel;
      }
      this.applyAIReportToState(data.report);
      this.renderAIBriefing(data.report, { preview: false });
      statusEl.innerText = "Full report generated";
      await this.pushState();
      finishFull();
      return true;
    } catch (e) {
      card.classList.add("hidden");
      if (!isPreview) finishFull();
      return false;
    }
  }

